Bg::Bg():
	m_bgPos(0, 0),
	m_mapChipPos(0, 0),
	m_cameraPos(0, 0),
	m_bgHandle(-1),
	m_mapHandle(-1),
	m_graphChipNumX(0),
	m_graphChipNumY(0)
{
	for (int x = 0; x < kChipNumX; x++)
	{
		for (int y = 0; y < kChipNumY; y++)
		{
			m_chipData[y][x] = kChipData[y][x];
		}
	}
}

Bg::~Bg()
{
}

void Bg::Init()
{
	// マップチップの数を数える
	int graphW = 0;
	int graphH = 0;
	GetGraphSize(m_bgHandle, &graphW, &graphH);

	// 座標の初期化
	m_bgPos.x = 0;
	m_bgPos.y = 0;
	m_mapChipPos.x = 0;
	m_mapChipPos.y = 0;
	m_cameraPos.x = 0;
	m_cameraPos.y = 0;

	m_graphChipNumX = graphW / kChipWidth;
	m_graphChipNumY = graphH / kChipHeight;
}

void Bg::Update()
{
	// プレイヤーを中心にカメラ位置を更新
	//m_cameraPos.x = m_playerPos.x / kChipWidth;
	//m_cameraPos.y = m_playerPos.y / kChipHeight;

	// カメラの範囲外を半分だけ計算する
	int startX = m_cameraPos.x - Game::kScreenWidth / 2;
	int startY = m_cameraPos.y - Game::kScreenWidth / 2;
	int endX = m_cameraPos.x - Game::kScreenHeight / 2;
	int endY = m_cameraPos.y + Game::kScreenHeight / 2;

	int pad = GetJoypadInputState(DX_INPUT_KEY_PAD1);

	if (pad & PAD_INPUT_LEFT) // ←を押した
	{
		// 背景を左に移動
		m_bgPos.x -= kSpeed;
		if (m_bgPos.x < 0)
		{
			m_bgPos.x = kLoopPos;
		}

		// マップチップを右に移動
		m_mapChipPos.x += kSpeed;
		if (m_mapChipPos.x < 0)
		{
			m_mapChipPos.x = 0;
		}

	}
	if (pad & PAD_INPUT_RIGHT) // →を押した
	{
		// 背景を右に移動
		m_bgPos.x += kSpeed;
		if (m_bgPos.x > kLoopPos)
		{
			m_bgPos.x = 0;
		}

		// マップチップを左に移動
		m_mapChipPos.x -= kSpeed;
		if (m_mapChipPos.x > Game::kScreenWidth * 2)
		{
			m_mapChipPos.x = 0;
		}
	}

	// マップチップの当たり判定を更新
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{
			// チップの値
 			int chipNo = m_chipData[y][x];

			// マップチップのグラフィック切り出し座標
			int srcX = kChipWidth * (chipNo % m_graphChipNumX);
			int srcY = kChipHeight * (chipNo / m_graphChipNumX);

			// 当たり判定を設定
			m_colRect[y][x].SetCenter(x * kChipWidth + kChipWidth / 2, y * kChipHeight + kChipHeight / 2, kChipWidth, kChipHeight);
			
		}
	}
}

void Bg::Draw()
{
	// 背景の描画
	DrawRectGraph(0, 0, m_bgPos.x, m_bgPos.y, Game::kScreenWidth, Game::kScreenHeight, m_bgHandle, false);

	// マップの描画
	for (int y = 0; y < kChipNumY; y++)
	{
		for (int x = 0; x < kChipNumX; x++)
		{
			// 設置するチップ
			int chipNo = m_chipData[y][x];

			// マップチップのグラフィック切り出し座標
			int srcX = kChipWidth * (chipNo % m_graphChipNumX);
			int srcY = kChipHeight * (chipNo / m_graphChipNumX);

			DrawRectGraph(x * kChipWidth, y * kChipHeight, srcX, srcY, kChipWidth, kChipHeight, m_mapHandle, true);

			if (chipNo == 1)
			{
				// 当たり判定の表示
				m_colRect[y][x].Draw(0xb0e0e6, false);
			}
		}
	}
}